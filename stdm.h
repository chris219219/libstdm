#ifndef STDM_H
#define STDM_H

#include <inttypes.h>
#include <stdlib.h>
#include <stdarg.h>
#include <stdbool.h>

/*
 * Use these types:
 *
 * char
 * bool
 * i8, i16, i32, i64
 * u8, u16, u32, u64
 * f32, f64
*/

typedef int8_t i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;

typedef uint8_t u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;

typedef float f32;
typedef double f64;

#endif